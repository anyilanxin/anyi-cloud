package com.anyilanxin.cloud.job.modules.jobcore.trigger;

import com.anyilanxin.cloud.corecommon.utils.AnYiI18nUtil;
import com.anyilanxin.cloud.job.modules.jobcore.conf.XxlJobAdminConfig;
import com.anyilanxin.cloud.job.modules.jobcore.route.ExecutorRouteStrategyEnum;
import com.anyilanxin.cloud.job.modules.jobcore.scheduler.XxlJobScheduler;
